class Retangle{
    private double height;
    private double base;
    private int age;

    public void setHeight(double h){
        this.height = h;
    }

    public void setBase(double b){
        this.base = b;
    }

    public void setAge(int a){
        this.age = a;
    }

    public double getHeight(){
        return this.height;
    }

    public double getBase(){
        return this.base;
    }

    public int getAge(){
        return this.age;
    }
}
